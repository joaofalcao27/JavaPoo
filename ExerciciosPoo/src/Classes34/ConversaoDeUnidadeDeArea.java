package Classes34;

public class ConversaoDeUnidadeDeArea {
		
		public static double metroParaPes(Double valor) {
			return valor * 10.76;			
		}
		public static double pesParaCm(Double valor) {
			return valor * 929;			
		}
		public static double milhaParaAcres(Double valor) {
			return valor * 10.76;			
		}
		public static double acreParaPes(Double valor) {
			return valor * 43.560;			
		}
}
